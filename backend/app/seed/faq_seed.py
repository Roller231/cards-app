async def seed_faqs(db: AsyncSession, only_if_empty: bool = True) -> int:
    if only_if_empty:
        existing_count = (await db.execute(select(func.count(FAQ.id)))).scalar() or 0
        if existing_count > 0:
            return 0

    for item in FAQ_DEFAULT_ITEMS:
        db.add(FAQ(**item))

    await db.commit()
    return len(FAQ_DEFAULT_ITEMS)
